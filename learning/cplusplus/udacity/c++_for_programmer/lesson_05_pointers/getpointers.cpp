#include <iostream>

int main()
{
    int a = 54;
    int b = 544;
    int c = 5444;
    
    std::cout<<"a = "<<a<<"\n";
    std::cout<<"b = "<<b<<"\n";
    std::cout<<"c = "<<c<<"\n";
    
    std::cout<<"address of a is at &a = "<< &a<<"\n";
    std::cout<<"address of b is at &b = "<< &b<<"\n";
    std::cout<<"address of c is at &c = "<< &c<<"\n";
    
    return 0;
}